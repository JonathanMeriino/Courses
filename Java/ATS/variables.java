public class variables {
    public static void main(String[] args) {
        //tipo de dato byte        -128 a 127
        byte entero=12;

        System.out.println("Numero entero: "+entero);
        //tipo de dato short
        short short_entero = 12345;

        System.out.println("Numero entero: "+short_entero);
        //Tipo de dato entero
        int int_entero = 1245656;

        System.out.println("Numero entero: "+int_entero);
        //Tipo de dato long
        long long_entero = 1245656;

        System.out.println("Numero entero: "+long_entero);

        //tipo de dato float
        float decimal = 3.452345f;

        System.out.println("Numero decimal: "+decimal);

        //tipo de dato caracter
        char caracter= 'a';

        System.out.println("Caracter: "+caracter);

        //tipo boolean
        boolean decision = true;

        System.out.println("La decision es: "+decision);

    }
}
