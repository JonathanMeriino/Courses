//Incremento y decremento
public class incdecrem {
    public static void main(String[] args) {
        int x=5, y=5;

        x++;
        System.out.println(x);

        y--;
        System.out.println(y);

        int a=5, b;
        b = ++a;

        System.out.println(b);

        b= --a;
        System.out.println(b);
    }
}
