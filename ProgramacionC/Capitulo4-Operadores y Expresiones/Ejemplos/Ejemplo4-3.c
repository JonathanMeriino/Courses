//Demostracion del funcionamiento de los operadores de incremento/decremento

#include<stdio.h>
//Test de operadores ++ y * /

void main()
{
    int m=45, n=75;
    printf("m = %d, n = %d\n",m,n);
    ++m;
    --n;
    printf("m= %d, n=%d\n",m,n);
    m++;
    n--;
    printf("m = %d,n = %d \n",m,n );

}
